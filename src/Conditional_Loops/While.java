package Conditional_Loops;

public class While {
    public static void main(String[] args) {
        int x = 0;
        int y = 5;

        while (x <= y) {
            System.out.println("Hello");
            x++;
        }

        System.out.println("================");

        int z = 1;

        while (z <= 7) {
            System.out.println("in a loop");
            z++;
        }


    }
}
